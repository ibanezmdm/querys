INSERT INTO [INFORMES3].[dbo].[CE_INSTOCK_EVOLUTIVO_RESUMEN]
           ([NOM_LOCAL]
           ,[COD_LOCAL]
           ,[DIVISION]
           ,[DEPARTAMENTO]
           ,[SUBDEP]
           ,[CLASE]
           ,[NOM_PROV]
           ,[RUT]
           ,[NUM_OH_VALORIZADO]
           ,[NUM_VTA_SEM_X_PERFIL]
           ,[VTA_PERDIDA_VP]
           ,[CANTIDAD_SKUS]
           ,[SEMANA])

SELECT
	A.NOM_LOCAL,
	A.COD_LOCAL,
	A.DIVISION,
	A.DEPARTAMENTO,
	A.SUBDEP,
	A.CLASE,
	A.NOM_PROV,
	A.RUT,
	SUM(A.NUM_OH_VALORIZADO) AS NUM_OH_VALORIZADO,
	SUM(A.NUM_VTA_SEM_X_PERFIL) AS NUM_VTA_SEM_X_PERFIL,
	SUM(A.VTA_PERDIDA_VP) AS VTA_PERDIDA_VP,
	SUM(A.CANTIDAD_SKUS) AS CANTIDAD_SKUS,
	A.SEMANA
FROM [INFORMES3].[dbo].[INSTOCK_NUEVA_PLANILLA_HISTORIA] AS A
WHERE 
	((A.DIVISION IN ('J01 - PGC COMESTIBLE', 'J02 - PGC NO COMESTIBLE', 'J05 - FLC'))
	AND (A.SIS_REPOSICION IN ('Reposicion x ASR', 'Informar a ASR')) 
	AND (A.IND_SURTIDO = 1) 
	AND A.SEMANA=(SELECT semana from [INFORMES3].dbo.DHW_MES_GC WHERE CONVERT(DATE,FECHA)=CONVERT(DATE,GETDATE()-7))) 
	OR ((A.DIVISION IN ('J06 - PANADERIA Y PASTELERIA', 'J07 - PLATOS PREPARADOS')) 
	AND (A.SIS_REPOSICION IN ('Reposicion x ASR'))
	AND (A.IND_SURTIDO = 1) 
	AND A.SEMANA=(SELECT semana from [INFORMES3].dbo.DHW_MES_GC WHERE CONVERT(DATE,FECHA)=CONVERT(DATE,GETDATE()-7)))
GROUP BY 
	A.NOM_LOCAL,
	A.COD_LOCAL,
	A.DIVISION,
	A.DEPARTAMENTO,
	A.SUBDEP,
	A.CLASE,
	A.NOM_PROV,
	A.RUT,
	A.SEMANA


